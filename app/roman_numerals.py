    if stuff == "I":
        return 1
    elif stuff == "II":
        return 2
    elif stuff == "III":
        return 3
    elif stuff == "IV":
        return 4
    elif stuff == "V":
        return 5
    elif stuff == "VI":
        return 6
    elif stuff == "VII":
        return 7
    elif stuff == "VIII":
        return 8
